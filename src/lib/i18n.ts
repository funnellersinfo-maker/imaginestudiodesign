export const useLang = create<LangStore>((set, get) => ({
  lang: "en",
  toggle: () => set((s) => ({ lang: s.lang === "en" ? "es" : "en" })),
  set: (lang) => set({ lang }),
  t: (key: string) => {
    const { lang } = get();
    return translations[lang]?.[key] ?? translations.en[key] ?? key;
  },
}));
